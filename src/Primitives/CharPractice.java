package Primitives;

public class CharPractice {

    public static void main(String[] args) {

        char firsLetter= 'A'; // value for a char data type needs to be in the single quotation mark

        System.out.println(firsLetter);

        char secondLetter='6';

        System.out.println(secondLetter);

        char thirdLetter= '^';
        System.out.println(thirdLetter);

        char forthLetter=66;
        System.out.println(forthLetter);
        // char forthLetter1= '66'; you can store just single character in ''

        char x=42;  // if you assign a number for a char data type, it will print the symbol from ASCII table
        System.out.println(x);

        double d = '?'; // it will print the number from ASCII table
        System.out.println(d);

        int k = 'f';
        System.out.println(k);

        int b = '2';
        System.out.println(b);

        int c='C', h='H', g='G';
        System.out.println(c+h+g);
        System.out.println(" "+c+h+g);
    }
}
